# Tipos primitivos em Python

# Inteiro (int)
idade = 30
numero = -15

# Ponto Flutuante (float)
altura = 1.75
peso = 68.5

# Booleano (bool)
tem_carteira = True
esta_chovendo = False

# Texto (string)
nome = "Ana"
mensagem = 'Olá, mundo!'


# Exibindo os valores das variáveis
print(f"Inteiro: idade = {idade}, numero = {numero}")
print(f"Ponto Flutuante: altura = {altura}, peso = {peso}")
print(f"Booleano: tem_carteira = {tem_carteira}, esta_chovendo = {esta_chovendo}")
print(f"Texto: nome = {nome}, mensagem = {mensagem}")

