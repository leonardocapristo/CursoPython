# Convertendo de Inteiro para Float
inteiro = 10
float_num = float(inteiro)
print("Inteiro para Float:", float_num)  # Output: 10.0

# Convertendo de Float para Inteiro
float_num = 10.5
inteiro = int(float_num)
print("Float para Inteiro:", inteiro)  # Output: 10

# Convertendo de String para Inteiro
string_num = "100"
inteiro = int(string_num)
print("String para Inteiro:", inteiro)  # Output: 100

# Convertendo de String para Float
string_num = "100.5"
float_num = float(string_num)
print("String para Float:", float_num)  # Output: 100.5

# Convertendo de Inteiro para String
inteiro = 20
string_num = str(inteiro)
print("Inteiro para String:", string_num)  # Output: "20"
